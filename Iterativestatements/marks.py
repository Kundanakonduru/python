n=int(input("Enter marks:"))
if n>1 and n<=100:
    
     if n>=90:
      print("A grade")
      
     elif n>=80:
       print("B grade")
     elif n>=70:
      print("c grade")
     elif n>=60:
      print("D grade")
     else:
      print("fail")
else:
    print("invalid")

            
